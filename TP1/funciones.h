#include <stdio.h>
#include <stdlib.h>




/** \brief Pide que se ingrese un valor, lo guarda y devuelve el valor inresado.
 *
 * \param [] char Muestra un mensaje.
 * \return int Devuelve un numero entero ingresado por el usuario.
 *
 */
int numeroEntero( char []);
/** \brief Toma los valores pedidos, y los suma.
 *
 * \param numeroUno int .Primer numero ingresado por el usuario.
 * \param numeroDos int .Segundo numero ingresado por el usuario.
 * \return Devuelve la suma de los dos valores ingresados por el usuario.
 *
 */
int sumar (int numeroUno, int numeroDos);
/** \brief Toma los valores pedidos y los resta.
 *
 * \param numeroUno int. Primer numero inresado por el ususario.
 * \param numeroDos int. Segundo numero ingresado por el usuario.
 * \return int Devuelve la resta de los dos valores ingresados por el usuario.
 *
 */
int restar(int numeroUno, int numeroDos);
/** \brief Toma los valores pedidos y los multiplica.
 *
 * \param numeroUno int. Primer numero inresado por el ususario.
 * \param numeroDos int. Segundo numero ingresado por el usuario.
 * \return int. Devuelve la multiplicacion de los dos valores ingresados por el usuario.
 *
 */
int multiplicar(int numeroUno, int numeroDos);
/** \brief Toma los valores pedidos y los divide.
 *
 * \param numeroUno int. Primer numero inresado por el ususario.
 * \param numeroDos int. Segundo numero ingresado por el usuario.
 * \return int. Devuelve la división de los dos valores ingresados por el usuario.
 *
 */
int dividir (int numeroUno, int numeroDos);
/** \brief Hace el factorial de los valores ingresados por el usuario.
 *
 * \param int. Primer numero ingresado por el usuario.
 * \param int. Segundo numero ingresado por el usuario
 * \return int. Devuelve el factorial del primer  y el segundo valor, junto a sus factorailes respectivamente.
 *
 */
int factorial(int, int);
/** \brief Toma el primer valor y hace el factorial.
 *
 * \param int. Primer numero ingresado por el usuario.
 * \return int. Devuelve el factorial del primer numero.
 *
 */
int factorial1(int);

